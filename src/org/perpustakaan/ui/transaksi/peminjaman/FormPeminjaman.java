        if(confirm == JOptionPane.OK_OPTION){
           
            
            Main.getpemiPeminjamanService().save(Main.peminjaman);
                       
            FormPeminjaman_Riwayat fpr = new FormPeminjaman_Riwayat();
            JDesktopPane desktopPane = getDesktopPane();
            desktopPane.add(fpr);
            fpr.setVisible(true);
            this.dispose();
        }
                
