    void showPeminjamanDetails(){
        Object[] column = {"Id Buku", "Judul Buku", "Pengarang"};
        Object [][] data = new Object[Main.peminjaman.getPeminjamanDetails().size()][3];
        int i = 0;
        for(PeminjamanDetail pd: Main.peminjaman.getPeminjamanDetails()){
            Object[] arr_data = {pd.getBuku().getId(), pd.getBuku().getJudul(), pd.getBuku().getPengarang()};
            data[i] = arr_data;
            i++;
        }
        tabmode = new DefaultTableModel(data, column);
        tabelPeminjamanDetail.setModel(tabmode);
    }
