    void showPengembalian(){
        Object[] column = {"Id Pengembalian", "Id Peminjaman", "Peminjam", "Tanggal Pinjam", "Tanggal Kembali"};
        Object[][] data = new Object[Main.getPengembalianService().getPengembalians().size()][5];
        int i = 0;
        for (Pengembalian p: Main.getPengembalianService().getPengembalians()){
            Object[] arr_data = {p.getId(), p.getPeminjaman().getId(), p.getPeminjaman().getAnggota().getNama()
                    , p.getPeminjaman().getTanggal(), p.getTanggal()};
            data[i] = arr_data;
            i++;
        }
        tabmode = new DefaultTableModel(data, column);
        tabelPengembalian.setModel(tabmode);
    }
    
