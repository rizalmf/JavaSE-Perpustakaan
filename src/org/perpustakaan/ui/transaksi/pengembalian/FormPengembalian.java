    private void bTandaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTandaiActionPerformed
        int tabel = tabelPengembalian.getSelectedRow();
        if(tabel == -1){
            JOptionPane.showMessageDialog(null, "pilih salah salah satu riwayat");
            return;
        }
        String nama = tabelPengembalian.getValueAt(tabel, 2).toString();
        int confirm = JOptionPane.showConfirmDialog(null, "Tandai "+nama+" telah mengembalikan buku? \n"
                + "Catatan: Riwayat yang telah ditandai tidak dapat dikembalikan", "Konfirmasi",JOptionPane.OK_CANCEL_OPTION);
        if(confirm == JOptionPane.OK_OPTION){
            Long id = Long.parseLong(tabelPengembalian.getValueAt(tabel, 0).toString());
            Main.pengembalian = Main.getPengembalianService().getPengembalian(id);
            Main.pengembalian.setTanggal(new Date());
            Main.getPengembalianService().save(Main.pengembalian);
            showPengembalian();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_bTandaiActionPerformed


