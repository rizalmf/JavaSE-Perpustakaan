    private DefaultTableModel tabmode;
    
    void showAnggota(){
        Object[] column = {"Id Anggota", "NIM", "Nama"};
        Object[][] data = new Object[Main.getAnggotaService().getAnggotas().size()][3];
        int i= 0;
        
        for(Anggota a: Main.getAnggotaService().getAnggotas()){
            Object[] arr_data = {a.getId(), a.getNim(), a.getNama()};
            data[i] = arr_data;
            i++;
        }
        tabmode = new DefaultTableModel(data, column);
        tabelAnggota.setModel(tabmode);
    }
