    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int tabel = tabelAnggota.getSelectedRow();
        Long id = 0l;
        String nim ="", nama = "";
        
        if(tabel == -1){
            JOptionPane.showMessageDialog(null, "Anggota belum dipilh");
            return;
        }
        try{
            id = Long.parseLong(tabelAnggota.getValueAt(tabel, 0).toString());
            nim = tabelAnggota.getValueAt(tabel, 1).toString();
            nama = tabelAnggota.getValueAt(tabel, 2).toString();
        } catch(Exception e){
            e.printStackTrace();
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Hapus "+nama+" dari anggota perpustakaan?", "Konfimasi", JOptionPane.OK_CANCEL_OPTION);
        
        if(confirm == JOptionPane.OK_OPTION){
            Main.anggota = new Anggota();
            Main.anggota.setId(id);
            Main.anggota.setNim(nim);
            Main.anggota.setNama(nama);
            Main.getAnggotaService().delete(Main.anggota);
            showAnggota();
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_bHapusActionPerformed

