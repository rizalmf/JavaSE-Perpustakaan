    private DefaultTableModel tabmode;
    void showByJudul(String judul){
        Object[] column = {"Id Buku", "Judul Buku", "Pengarang", "Penerbit"};
        Object[][] data = new Object[Main.getBukuService().getByJudul(judul).size()][4];
        int i = 0;
        for(Buku b:Main.getBukuService().getByJudul(judul)){
            Object [] arr_data = {b.getId(), b.getJudul(), b.getPengarang(), b.getPenerbit()};
            data[i] = arr_data;
            i++;
        }
        tabmode = new DefaultTableModel(data, column);
        tabelBuku.setModel(tabmode);
    }
