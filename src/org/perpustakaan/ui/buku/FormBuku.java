    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int tabel = tabelBuku.getSelectedRow();
        Long id = 0l;
        String judul ="", pengarang = "", penerbit="";
        if(tabel == -1){
            JOptionPane.showMessageDialog(null, "Buku belum dipilh");
            return;
        }
        try{
            id = Long.parseLong(tabelBuku.getValueAt(tabel, 0).toString());
            judul = tabelBuku.getValueAt(tabel, 1).toString();
            pengarang = tabelBuku.getValueAt(tabel, 2).toString();
            penerbit = tabelBuku.getValueAt(tabel, 3).toString();
        } catch(Exception e){
            e.printStackTrace();
        }
        
        int confirm = JOptionPane.showConfirmDialog(null, "Hapus Buku "+judul+"?", "Konfimasi", JOptionPane.OK_CANCEL_OPTION);
        
        if(confirm == JOptionPane.OK_OPTION){
            Main.buku = new Buku();
            Main.buku.setId(id);
            Main.buku.setJudul(judul);
            Main.buku.setPengarang(pengarang);
            Main.buku.setPenerbit(penerbit);
            Main.getBukuService().delete(Main.buku);
            showBuku();
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_bHapusActionPerformed

